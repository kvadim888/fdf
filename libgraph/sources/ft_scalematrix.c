#include "libgraph.h"

void	ft_scalematrix(double mat[3][3], t_vect vect)
{
	mat[0][0] = vect.x;
	mat[0][1] = 0;
	mat[0][2] = 0;
	mat[1][0] = 0;
	mat[1][1] = vect.y;
	mat[1][2] = 0;
	mat[2][0] = 0;
	mat[2][1] = 0;
	mat[2][2] = vect.z;
}
